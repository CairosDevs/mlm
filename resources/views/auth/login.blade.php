<script>
    function reloadCaptcha() {
        let url = "{{ url('/') }}";
        $.ajax({
            type:'GET',
            url:url+'/reloadCaptcha',
            success:function (data) {
                $(".captcha span").html(data.captcha)
            }
        })
    }
</script>